function attachEquipmentStateHandlers(): void {
  document.querySelectorAll<HTMLButtonElement>(".equipment-state-button").forEach((button) => {
    button.addEventListener("click", () => {
      const id = Number(button.dataset.equipmentId);
      const state = button.dataset.equipmentState as EquipmentState | undefined;
      const status = document.querySelector<HTMLElement>("#equipment-state-status");

      if (!Number.isFinite(id) || !state) {
        return;
      }

      button.disabled = true;
      if (status) {
        status.textContent = "";
      }

      fetch(api.equipmentStateUrl(id), {
        method: "PATCH",
        headers: { "content-type": "application/json" },
        body: JSON.stringify({ state })
      })
        .then(async (response) => {
          if (!response.ok) {
            throw new Error(await responseErrorMessage(response, "equipment state"));
          }

          if (status) {
            status.textContent = t("equipmentStateUpdated");
          }

          return refreshConsole();
        })
        .catch((error: unknown) => {
          button.disabled = false;
          if (status) {
            const detail = error instanceof Error ? error.message : String(error);
            status.textContent = `${t("equipmentStateFailed")}: ${detail}`;
          }
        });
    });
  });
}

function attachSideRailHandlers(): void {
  const buttons = Array.from(document.querySelectorAll<HTMLButtonElement>(".side-nav-button"));

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      const targetId = button.dataset.target ?? "";
      const target = targetId ? document.getElementById(targetId) : null;

      buttons.forEach((item) => item.classList.toggle("is-active", item === button));

      if (!target) {
        return;
      }

      if (typeof target.scrollIntoView === "function") {
        target.scrollIntoView({
          behavior: "smooth",
          block: "start"
        });
      } else {
        window.location.hash = targetId;
      }
    });
  });
}

