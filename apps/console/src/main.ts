function readStoredLanguage(): LanguageCode {
  if (typeof localStorage === "undefined") {
    return "zh";
  }

  return localStorage.getItem(LANGUAGE_STORAGE_KEY) === "en" ? "en" : "zh";
}

export function setConsoleLanguage(language: LanguageCode): void {
  consoleLanguage = language;

  if (typeof localStorage !== "undefined") {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  }
}

function t(key: keyof typeof translations.en, replacements: Record<string, string> = {}): string {
  let value = translations[consoleLanguage][key];

  for (const [replacementKey, replacementValue] of Object.entries(replacements)) {
    value = value.replaceAll(`{${replacementKey}}`, replacementValue);
  }

  return value;
}

function escapeHtml(value: string): string {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function fallbackThoughtSummary(analysis: DailyBattleAnalysis): string {
  const sections = [
    ...analysis.coreThemes,
    ...analysis.repeatedQuestions,
    ...analysis.newlyFormedJudgments,
    ...analysis.unclosedThinkingLoops,
    ...analysis.reusableMaterial,
    ...analysis.threadsToContinueTomorrow
  ].filter((item) => item.trim().length > 0);

  if (sections.length === 0) {
    return "今天还没有足够的捕获内容生成战况分析。AGE-FX 会继续等待 ChatGPT 和 Gemini 的对话记录，正式分析仍然在本地零点结算刚刚结束的昨天。";
  }

  return `今天的战况来自旧版结构化记录。主要线索是：${sections.join(" ")}\n\n这份兼容摘要不会回放原始聊天记录；新的结算和预览会直接生成更完整的长文战况分析。`;
}

function renderThoughtSummary(analysis: DailyBattleAnalysis): string {
  const summary = analysis.thoughtSummary?.trim() || fallbackThoughtSummary(analysis);
  const paragraphs = summary
    .split(/\n{2,}/)
    .map((paragraph) => paragraph.trim())
    .filter((paragraph) => paragraph.length > 0);

  return `
    <section class="summary-panel" id="battle-analysis" aria-label="Daily battle summary">
      <p class="eyebrow">${escapeHtml(t("dailyBattleAnalysis"))}</p>
      <h2>${escapeHtml(analysis.thoughtTitle)}</h2>
      <div class="summary-copy">
        ${paragraphs.map((paragraph) => `<p>${escapeHtml(paragraph)}</p>`).join("")}
      </div>
    </section>
  `;
}

function firstEquipment(
  recommendedEquipment: DailyBattleAnalysis["recommendedEquipment"]
): EquipmentRecommendation | null {
  return Array.isArray(recommendedEquipment)
    ? recommendedEquipment[0] ?? null
    : recommendedEquipment;
}

function mergeArchivedEquipment(
  analysis: DailyBattleAnalysis | null,
  items: EquipmentRecommendation[] = []
): EquipmentRecommendation | null {
  const recommendation = analysis ? firstEquipment(analysis.recommendedEquipment) : null;

  if (!recommendation) {
    return null;
  }

  const archivedItem = items.find(
    (item) =>
      item.analysisDate === analysis?.analysisDate &&
      item.equipmentName === recommendation.equipmentName &&
      item.equipmentType === recommendation.equipmentType
  );

  return {
    ...recommendation,
    ...archivedItem
  };
}

