export function createConsoleApi(baseUrl: string) {
  return {
    statusUrl(date: string): string {
      return `${baseUrl}/api/status?date=${encodeURIComponent(date)}`;
    },
    settledAnalysisUrl(date: string): string {
      return `${baseUrl}/api/analysis?date=${encodeURIComponent(date)}`;
    },
    previewUrl(): string {
      return `${baseUrl}/api/preview`;
    },
    manualPromptUrl(date: string): string {
      return `${baseUrl}/api/manual-analysis/prompt?date=${encodeURIComponent(date)}`;
    },
    manualPreviewUrl(): string {
      return `${baseUrl}/api/manual-analysis/preview`;
    },
    manualSettleUrl(): string {
      return `${baseUrl}/api/manual-analysis/settle`;
    },
    runtimeConfigUrl(): string {
      return `${baseUrl}/api/settings/runtime-config`;
    },
    equipmentUrl(): string {
      return `${baseUrl}/api/equipment`;
    },
    equipmentStateUrl(id: number): string {
      return `${baseUrl}/api/equipment/${encodeURIComponent(String(id))}/state`;
    },
    refreshPlan(mode: ConsoleMode, today: string, settledDate: string) {
      const statusUrl = this.statusUrl(today);

      if (mode === "preview") {
        return {
          mode,
          statusUrl,
          previewUrl: this.previewUrl()
        };
      }

      return {
        mode,
        statusUrl,
        settledAnalysisUrl: this.settledAnalysisUrl(settledDate)
      };
    }
  };
}

