function setManualBridgeStatus(date: string, message: string): void {
  const status = document.querySelector<HTMLElement>(`#${CSS.escape(bridgeId(date, "status"))}`);

  if (status) {
    status.textContent = message;
  }
}

async function copyManualPrompt(date: string): Promise<void> {
  const response = await fetch(api.manualPromptUrl(date));

  if (!response.ok) {
    throw new Error(await responseErrorMessage(response, "manual prompt"));
  }

  const payload = (await response.json()) as ManualPromptResponse;
  const output = document.querySelector<HTMLTextAreaElement>(
    `#${CSS.escape(bridgeId(date, "prompt-output"))}`
  );

  if (output) {
    output.value = payload.prompt;
  }

  if (navigator.clipboard) {
    try {
      await navigator.clipboard.writeText(payload.prompt);
      setManualBridgeStatus(date, t("promptCopied"));
      return;
    } catch {
      setManualBridgeStatus(date, t("promptGeneratedClipboardBlocked"));
      return;
    }
  }

  setManualBridgeStatus(date, t("promptGenerated"));
}

async function submitManualAnalysis(
  date: string,
  status: StatusResponse,
  mode: "preview" | "settle"
): Promise<void> {
  const input = document.querySelector<HTMLTextAreaElement>(
    `#${CSS.escape(bridgeId(date, "analysis-input"))}`
  );
  const analysisText = input?.value.trim() ?? "";

  if (!analysisText) {
    setManualBridgeStatus(date, t("pasteModelJsonFirst"));
    return;
  }

  const response = await fetch(
    mode === "preview" ? api.manualPreviewUrl() : api.manualSettleUrl(),
    {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify({ date, analysisText })
    }
  );

  if (!response.ok) {
    throw new Error(await responseErrorMessage(response, `manual ${mode}`));
  }

  const payload = (await response.json()) as AnalyzeResponse;

  if (!payload.analysis) {
    throw new Error("AGE-FX manual bridge response did not include analysis.");
  }

  if (mode === "settle") {
    currentMode = "collection";
    await refreshConsole();
    return;
  }

  renderAnalysis(payload.analysis, status, "manual-preview");
}

