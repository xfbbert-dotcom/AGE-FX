export async function responseErrorMessage(
  response: Response,
  label: string
): Promise<string> {
  let detail = "";

  try {
    const payload = (await response.clone().json()) as {
      message?: string;
      error?: string;
    };
    detail = payload.message ?? payload.error ?? "";
  } catch {
    detail = await response
      .clone()
      .text()
      .catch(() => "");
  }

  return detail
    ? `AGE-FX ${label} returned ${response.status}: ${detail}`
    : `AGE-FX ${label} returned ${response.status}`;
}

async function refreshConsole(): Promise<void> {
  const date = todayIsoDate();
  const settledDate = previousIsoDate();
  const plan = api.refreshPlan(currentMode, date, settledDate);
  const [statusResponse, runtimeConfigResponse] = await Promise.all([
    fetch(plan.statusUrl),
    fetch(api.runtimeConfigUrl())
  ]);

  if (!statusResponse.ok) {
    throw new Error(await responseErrorMessage(statusResponse, "status"));
  }

  const status = (await statusResponse.json()) as StatusResponse;
  const runtimeConfig = runtimeConfigResponse.ok
    ? ((await runtimeConfigResponse.json()) as RuntimeConfig)
    : null;

  if (plan.mode === "preview") {
    await renderPreview(date, status);
    return;
  }

  const settledAnalysisResponse = await fetch(plan.settledAnalysisUrl);
  let settledAnalysis: DailyBattleAnalysis | null = null;
  let settledEquipment: EquipmentRecommendation | null = null;

  if (settledAnalysisResponse.ok) {
    const payload = (await settledAnalysisResponse.json()) as AnalyzeResponse;
    settledAnalysis = payload.analysis ?? null;
    settledEquipment = settledAnalysis ? firstEquipment(settledAnalysis.recommendedEquipment) : null;
    const equipmentResponse = await fetch(api.equipmentUrl());

    if (equipmentResponse.ok) {
      const equipmentItems = ((await equipmentResponse.json()) as EquipmentListResponse).items;
      settledEquipment = mergeArchivedEquipment(settledAnalysis, equipmentItems);
    }
  } else if (settledAnalysisResponse.status !== 404) {
    throw new Error(await responseErrorMessage(settledAnalysisResponse, "analysis"));
  }

  renderCollectionStatus(
    date,
    settledDate,
    status,
    settledAnalysis,
    runtimeConfig,
    null,
    settledEquipment
  );
}

async function renderPreview(date: string, status: StatusResponse): Promise<void> {
  const response = await fetch(api.previewUrl(), {
    method: "POST",
    headers: { "content-type": "application/json" },
    body: JSON.stringify({ date })
  });

  if (!response.ok) {
    throw new Error(await responseErrorMessage(response, "preview"));
  }

  const payload = (await response.json()) as AnalyzeResponse;

  if (!payload.analysis) {
    throw new Error("AGE-FX preview response did not include analysis.");
  }

  renderAnalysis(payload.analysis, status, "preview");
}

async function previewToday(date: string): Promise<void> {
  const statusResponse = await fetch(api.statusUrl(date));

  if (!statusResponse.ok) {
    throw new Error(await responseErrorMessage(statusResponse, "status"));
  }

  await renderPreview(date, (await statusResponse.json()) as StatusResponse);
}

